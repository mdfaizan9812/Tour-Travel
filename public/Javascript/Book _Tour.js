var colour="white";
var blk="";

function focussource(){
	document.querySelector(".source input").style.background = colour;
}

function blursource(){
	document.querySelector(".source input").style.background = blk;
}

function focusdestination(){
	document.querySelector(".destination input").style.background = colour;
}

function blurdestination(){
	document.querySelector(".destination input").style.background = blk;
}

